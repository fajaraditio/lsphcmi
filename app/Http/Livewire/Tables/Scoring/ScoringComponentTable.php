    {
        return [
            Column::make('ID', 'id')
                ->searchable()
                ->sortable(),

            Column::make('Kompetensi', 'title')
                ->searchable()
                ->sortable(),

            Column::make('Bobot', 'weight')
                ->searchable()
                ->sortable(),

            Column::make('Jumlah Kriteria', 'scoring_criterias_count')
                ->searchable()
                ->sortable(),
