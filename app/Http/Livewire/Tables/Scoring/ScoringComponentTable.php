    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('title')
            ->addColumn('created_at')
            ->addColumn('created_at_formatted', fn (ScoringComponent $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'));
    }

